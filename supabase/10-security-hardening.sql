-- ========================================================================
-- C. authenticated may no longer write the legacy user_id column
-- ========================================================================
-- Row policies still gate by user_uuid; this closes the column Hibernate owns.
-- The trigger (SECURITY DEFINER, runs as table owner) still fills user_id.
revoke insert, update on public.task from authenticated;
grant insert (task_id, title, description, due_date, creation_date,
              is_completed, subject, subject_color, category, category_color,
              priority, user_uuid, deleted_at)
  on public.task to authenticated;
grant update (task_id, title, description, due_date, creation_date,
              is_completed, subject, subject_color, category, category_color,
              priority, user_uuid, deleted_at)
  on public.task to authenticated;

-- ========================================================================
-- D. completions must reference YOUR recurring task
-- ========================================================================
drop policy if exists "insert own completions" on public.recurring_completion;
create policy "insert own completions" on public.recurring_completion
  for insert to authenticated
  with check (
    auth.uid() = user_uuid
    and exists (
      select 1 from public.recurring_task rt
      where rt.id = recurring_task_id
        and rt.user_uuid = auth.uid()
    )
  );

-- ========================================================================
-- E. avatars bucket: images only, 1 MB cap
-- ========================================================================
update storage.buckets
set file_size_limit = 1048576,
    allowed_mime_types = array['image/jpeg', 'image/png', 'image/webp']
where id = 'avatars';

-- ========================================================================
-- F. events: end may not precede start (belt for the client-side check)
-- ========================================================================
do $$
begin
  if not exists (
    select 1 from pg_constraint where conname = 'event_end_after_start'
  ) then
    alter table public.event
      add constraint event_end_after_start check (end_at is null or end_at >= start_at);
  end if;
end $$;

